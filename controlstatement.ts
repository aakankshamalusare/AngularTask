var cnt = 21;

if(cnt<4){

    console.log("Condition is true");

}else{

    console.log("Condition is false");
}

cnt = 2;

switch (cnt) {
    
    case 1:
        console.log("In Case 1");
        break;

    case 2:
        console.log("In Case 2");
        break;

    default:
        console.log("In Default");
        break;
}

